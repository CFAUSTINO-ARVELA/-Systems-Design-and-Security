package university;

public class ModuleChoice {
	
	private int registrationNumber;
	private String moduleCode;
	private char period;
	private int grade;
	
	public int getRegistrationNumber() {
		return registrationNumber;
	}
	public String getModuleCode() {
		return moduleCode;
	}
	public char getPeriod() {
		return period;
	}
	public int getGrade() {
		return grade;
	}

}
